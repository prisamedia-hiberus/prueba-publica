public class Spade {

	private String player;
	private int playerId;
	private int playerProvId;
	private int punt;
	private int dorsal;
	private String url;
	private String urlPhoto;

	/**
	 * @return the player
	 */
	public String getPlayer() {
		return player;
	}

	/**
	 * @param player
	 *            the player to set
	 */
	public void setPlayer(String player) {
		this.player = player;
	}

	/**
	 * @return the playerId
	 */
	public int getPlayerId() {
		return playerId;
	}

	/**
	 * @param playerId
	 *            the playerId to set
	 */
	public void setPlayerId(int playerId) {
		this.playerId = playerId;
	}

	/**
	 * @return the playerProvId
	 */
	public int getPlayerProvId() {
		return playerProvId;
	}

	/**
	 * @param playerProvId
	 *            the playerProvId to set
	 */
	public void setPlayerProvId(int playerProvId) {
		this.playerProvId = playerProvId;
	}

	/**
	 * @return the punt
	 */
	public int getPunt() {
		return punt;
	}

	/**
	 * @param punt
	 *            the punt to set
	 */
	public void setPunt(int punt) {
		this.punt = punt;
	}

	/**
	 * @return the dorsal
	 */
	public int getDorsal() {
		return dorsal;
	}

	/**
	 * @param dorsal
	 *            the dorsal to set
	 */
	public void setDorsal(int dorsal) {
		this.dorsal = dorsal;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url
	 *            the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * @return the urlPhoto
	 */
	public String getUrlPhoto() {
		return urlPhoto;
	}

	/**
	 * @param urlPhoto
	 *            the urlPhoto to set
	 */
	public void setUrlPhoto(String urlPhoto) {
		this.urlPhoto = urlPhoto;
	}

}
