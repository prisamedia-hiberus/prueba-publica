package com.diarioas.guialigas.dao.model.video;

import java.util.Date;

public class VideoItem {

	private String author;
	private String description;
	private String link;
	private String title;
	private Date datePub;
	private String urlEnclosure;
	private String urlThumbnail;
	/**
	 * @return the author
	 */
	public String getAuthor() {
		return author;
	}
	/**
	 * @param author the author to set
	 */
	public void setAuthor(String author) {
		this.author = author;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the link
	 */
	public String getLink() {
		return link;
	}
	/**
	 * @param link the link to set
	 */
	public void setLink(String link) {
		this.link = link;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the datePub
	 */
	public Date getDatePub() {
		return datePub;
	}
	/**
	 * @param datePub the datePub to set
	 */
	public void setDatePub(Date datePub) {
		this.datePub = datePub;
	}
	/**
	 * @return the urlEnclosure
	 */
	public String getUrlEnclosure() {
		return urlEnclosure;
	}
	/**
	 * @param urlEnclosure the urlEnclosure to set
	 */
	public void setUrlEnclosure(String urlEnclosure) {
		this.urlEnclosure = urlEnclosure;
	}
	/**
	 * @return the urlThumbnail
	 */
	public String getUrlThumbnail() {
		return urlThumbnail;
	}
	/**
	 * @param urlThumbnail the urlThumbnail to set
	 */
	public void setUrlThumbnail(String urlThumbnail) {
		this.urlThumbnail = urlThumbnail;
	}

	
	
}
