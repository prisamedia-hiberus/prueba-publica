package com.diarioas.guialigas.dao.model.team;

public class Article {

	private String author;
	private String charge;
	private String title;
	private String subTitle;
	private String body;
	private String urlVideo;
	private String urlVideoImage;

	/**
	 * @return the author
	 */
	public String getAuthor() {
		return author;
	}

	/**
	 * @param author
	 *            the author to set
	 */
	public void setAuthor(String author) {
		this.author = author;
	}

	/**
	 * @return the charge
	 */
	public String getCharge() {
		return charge;
	}

	/**
	 * @param charge
	 *            the charge to set
	 */
	public void setCharge(String charge) {
		this.charge = charge;
	}

	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * @param title
	 *            the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * @return the subTitle
	 */
	public String getSubTitle() {
		return subTitle;
	}

	/**
	 * @param subTitle
	 *            the subTitle to set
	 */
	public void setSubTitle(String subTitle) {
		this.subTitle = subTitle;
	}

	/**
	 * @return the body
	 */
	public String getBody() {
		return body;
	}

	/**
	 * @param body
	 *            the body to set
	 */
	public void setBody(String body) {
		this.body = body;
	}

	/**
	 * @return the urlVideo
	 */
	public String getUrlVideo() {
		return urlVideo;
	}

	/**
	 * @param urlVideo
	 *            the urlVideo to set
	 */
	public void setUrlVideo(String urlVideo) {
		this.urlVideo = urlVideo;
	}

	/**
	 * @return the urlVideoImage
	 */
	public String getUrlVideoImage() {
		return urlVideoImage;
	}

	/**
	 * @param urlVideoImage
	 *            the urlVideoImage to set
	 */
	public void setUrlVideoImage(String urlVideoImage) {
		this.urlVideoImage = urlVideoImage;
	}

}
